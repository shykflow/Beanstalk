class Command(BaseCommand):
    def add_arguments(self, parser: CommandParser):
        parser.add_argument('--local', action='store_true', default=False)

    def handle(self, *args, **kwargs):
        all_experiences = Experience.objects.all()
        num_exps = all_experiences.count()
        locations: list[tuple[str, tuple]] = list(
            LOCAL_LOCATIONS_AND_COORDINATES.items()
            if kwargs['local'] else
            WORLDWIDE_LOCATIONS_AND_COORDINATES.items()
        )
        func = random.choices if num_exps > len(locations) else random.sample
        random_chosen_locations = func(locations, k=num_exps)
        assert num_exps == len(random_chosen_locations)
        with CommandLineSpinner(label='Saving random locations for experiences'):
            for exp, location in zip(all_experiences, random_chosen_locations):
                exp.location = location[0]
                exp.latitude = location[1][0]
                exp.longitude = location[1][1]
                exp.save()
                update_experience_latlong_one_to_one_ref(exp)
