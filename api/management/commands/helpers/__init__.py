AXIS_DIRECTIONS = ('N', 'S', 'E', 'W')

REGEX = {
    # Matches:
    # $
    # $$
    # $$$
    # $$$$
    'cost_dollars': r"^[\$]{1,4}$",

    # Matches:
    # 41.56717, -93.64406
    # 35.83756, -75.91912
    # 35.83756,-75.91912
    'geo_lat_long': r"^\s*[+-]?\d{1,3}([.]\d+)?\s*[,]\s*[+-]?\d{1,3}([.]\d+)?\s*$",

    # Matches
    # 41.56717 N, -93.64406 W
    'geo_lat_long_can_adapt': r"^\s*[+-]?\d{1,3}([.]\d+)?\s*[NS]?\s*[,]\s*[+-]?\d{1,3}([.]\d+)?\s*[EW]?s*$",

    # Matches:
    # N38 16 0 E0 43 0
    # N38 2 11.796 E38 45 49.284
    'geo_hour_min_sec': r"^\s*[NS]\d{1,3}\s+\d{1,2}\s+\d{1,2}([.]\d+)?\s+[EW]\d{1,3}\s+\d{1,2}\s+\d{1,2}([.]\d+)?\s*$",

    # Matches:
    # 30°00′24″N, 95°33′40″W
    # 30°00′41″N, 97°15′51″W
    # 30°01′19″N, 97°47′36″W
    # 30°02′41″N, 102°06′52″W
    # 30°04′58.04″N, 82°07′21.27″W
    'geo_degree_quote': r"^" +
        r"\d{1,3}([.]\d+)?°" +    # 1.2°
        r"(\d{1,2}([.]\d+)?′)?" + # 1.2′
        r"(\d{1,2}([.]\d+)?″)?" + # 1.2″
        r"[NS](,)?\s*" +          # N,
        r"\d{1,3}([.]\d+)?°" +    # 1.2°
        r"(\d{1,2}([.]\d+)?′)?" + # 1.2′
        r"(\d{1,2}([.]\d+)?″)?" + # 1.2″
        r"[EW]" +                 # E
        r"$"
}
